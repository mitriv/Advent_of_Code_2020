def count_trees_hit(inputs, coord):
	trees = 0
	x = 0
	for line in inputs[::coord[1]]:
		# be smarter and modulo the line len
		if x >= len(line):
			x = x % len(line)	
		if line[x] == "#":
			trees += 1
		x += coord[0]

	return trees


print(f"Part1: {count_trees_hit(inputs, (3,1))}")

from functools import reduce
print(f"Part2: {reduce(lambda x, y: x * y, [count_trees_hit(inputs, coord) for coord in [(1,1),(3,1),(5,1),(7,1),(1,2)]])}")
